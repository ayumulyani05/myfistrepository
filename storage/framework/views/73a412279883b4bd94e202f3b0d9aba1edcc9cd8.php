    <p class="card-text"><b>1. Alur Deteksi Covid-19</b></p>
    <p class="card-text">Cari Tahu Langkah-Langkah Yang Perlu Anda Lakukan Jika Anda Mengalami Gejala Demam. </p>
    <p class="card-text">Anda tidak perlu panik jika terkena atau merasa tidak enak badan yang disertai peningkatan suhu tubuh.</p>
    <p class="card-text">Anda bisa mengetahui apakah anda cukup beristirahat saja sambil memantau kondisi tubuh selama beberapa hari ke depan atau memang perlu berkonsultasi dengan dokter.</p>
    <p class="card-text"><b>2. Muncul Gejala batuk dan gangguan pernapasan</b></p>
    <p class="card-text">Pemeriksakan diri kefasilitas pelayanan kesehatan (fasyankes) jika demam tidak kunjung membaik setelah tujuh hari. periksakan pula diri anda jika demam diperparah dengan keluhan lain seperti batuk,pileknyeri tenggorokan, ataupun gangguan pernapasan.</p>
    <p class="card-text"><b>3. Diketahui Memiliki Riwayat Kontak dengan pasien Positif COVID-19.</b></p>
    <P class="card-text">Di fasyankes, dokter akan mengeluvaluasikan keadaan klinis anda dengan menentukan apakah anda masuk ke dalam kriteria pasien dalam pengawasan (PDP). jika tidak, anda akan mendapatkan petawatan sesuai diagonis dokter,baik berpa rawat jalan atau rawat inap. akan tetapi jika hasil pemeriksaan menunjukan bahwa anda merupakan PDP, anda akan dirujuk salah satu RS rujukan dengan didampingi oleh petugas kesehatan yang menggunakan alat pelindung diri (APD). PDP yang memiliki riwayat kontak dengan orang yang dinyatakan positif COVID-19 akan digolongkan sebagai kasus suspek.selanjutnya pasien ditempatkan diruang isolasi sambil menunggu pemeriksaan spesimen.</P>
    <p class="card-text"><b>4. Pemeriksaan Spesimen</b></p>
    <p class="card-text">Baik PDP maupun suspek COVID-19 akan melalui pemeriksaan spesimen. Menurut Staf Mikrobiologi Departemen Patologi Klinik RSUP Dr. Hasan Sadikin Bandung, terdapat 3 spesimen yang wajib diambil. Spesimen pertama berupa sediaan apusan dari saluran nasofaring (bagian atas tenggorokan) dan orofaring (tenggorokan). Spesimen kedua yang wajib diambil adalah dahak, aspirat trakea atau bronchoalveolar lavage (BAL) yang berasal dari saluran napas bagian bawah. Spesimen ketiga yaitu spesimen serum sebanyak 2 ml yang diambil dari 5 ml darah. Pemeriksaan ini dilakukan di Badan Penelitian dan Pengembangan Kesehatan (Balitbangkes) menggunakan metode polymerase chain reaction (PCR) dan Genom Sekuensing. Hasil pertama dari pemeriksaan tersebut akan keluar dalam waktu 24 jam.</p>
    <p class="card-text"><b>5. Tindakan yang Diambil Berdasarkan Hasil Tes</b></p>
    <P class="card-text">PDP maupun Suspek COVID-19 akan dipulangkan jika mendapatkan hasil negatif dari pemeriksaan spesimen. Namun demikian, pemantauan berkala akan tetap dilakukan untuk memastikan kesehatan yang bersangkutan dan orang-orang di sekitar. Lain halnya jika hasil pemeriksaan tes spesimen tersebut positif. Dalam hal ini, pasien akan dirawat di ruang isolasi RS rujukan dan menjalani pemantauan ketat hingga sembuh.</P>
