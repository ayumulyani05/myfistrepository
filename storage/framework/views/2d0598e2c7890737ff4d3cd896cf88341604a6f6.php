    <div class="card-header" align="center"><b>Mitos Dan Fakta Covid-19</b></div>
    <div class="card-body">
    <p class="card-text">Terdapat banyak kesalahpahaman masyarakat terkait virus ini. Berikut adalah mitos dan fakta tentang virus Corona menurut Organisasi Kesehatan Dunia (WHO) yang perlu diketahui:
</p>
    <p class="card-text">1. Mitos: pengering tangan efektif untuk membunuh virus Covid-19
Faktanya, pengering tangan tidak efektif dalam membunuh virus Corona terbaru tersebut. Untuk melindungi dari virus tersebut, disarankan agar Anda sering membersihkan tangan dengan sabun dan air. Namun, Anda juga dapat menggunakan cairan pembesih tangan (hand sanitizer) yang
mengandung alkohol. Setelah itu, keringkan dengan tisu atau pengering tangan. Mencuci tangan dengan sabun secara teratur ini dapat meminimalisir risiko masuknya virus ke dalam tubuh.</p>
    <p class="card-text">2. Mitos: lampu desinfeksi ultraviolet (UV) dapat membunuh virus Covid-19
Beberapa rumah sakit memang menggunakan lampu ini untuk membunuh mikroba pada alat-alat kesehatan seperti di ruang operasi atau di laboratorium. Namun, WHO tidak menyarankan bagi Anda menggunakan lampu UV untuk mensterilkan tangan atau area kulit lainnya karena radiasi sinar UV dapat menyebabkan kulit menjadi iritasi..</p>
    <p class="card-text">3. Mitos: pemindai suhu atau thermal detector efektif dalam mendeteksi orang yang terinfeksi Covid-19
Alat ini hanya bisa mendeteksi orang dengan virus Corona yang mengalami gejala demam. Pasalnya, pemindai suhu tersebut bekerja untuk mendeteksi adanya perbedaan suhu. Orang yang memiliki suhu tubuh lebih tinggi dari normal karena infeksi virus ini dapat 
terdeteksi. Sebaliknya, pendeteksi suhu tidak dapat mendeteksi orang yang terinfeksi tetapi belum sakit demam. Dilansir dari CDC, dibutuhkan antara 2 hingga 14 hari sebelum orang yang terinfeksi menjadi sakit dan mengalami demam.
</p>
    <p class="card-text">4. Mitos: menyemprotkan alkohol atau klorin ke seluruh tubuh dapat membunuh virus Covid-19
Faktanya, menyemprotkan alkohol atau klorin ke seluruh tubuh Anda tidak akan membunuh virus yang telah memasuki tubuh Anda. Menyemprotkan zat-zat semacam itu bisa berbahaya bagi pakaian atau selaput lendir tubuh seperti mata dan mulut. Ketahuilah bahwa
alkohol dan klorin dapat berguna untuk mendisinfeksi permukaan, tetapi perlu digunakan di bawah rekomendasi yang tepat.</p>
    <p class="card-text">5. Mitos: tidak aman menerima surat atau paket dari China. Hal ini tidak benar.
Faktanya, orang yang menerima paket dari China tidak berisiko tertular virus corona. Dari analisis sebelumnya, coronavirus tidak akan bertahan lama pada objek, seperti surat atau paket menurut WHO.</p>
    <p class="card-text">6. Mitos: hewan peliharaan di rumah dapat menyebarkan Covid-19
Hingga saat ini, tidak ada bukti bahwa hewan peliharaan seperti anjing atau kucing dapat terinfeksi virus corona baru atau Covid-19 ini. Namun, wajib mencuci tangan dengan sabun dan air setelah kontak dengan hewan peliharaan. Ini melindungi Anda terhadap berbagai bakteri
umum, seperti E.coli dan Salmonella, yang dapat berpindah di antara hewan peliharaan dan manusia.</p>
    <p class="card-text">7. Mitos: vaksin pneumonia dapat melindungi dari virus Covid-19
Meski keduanya merupakan penyakit pada sistem pernapasan, faktanya vaksin pneumonia, seperti vaksin pneumokokus dan vaksin Haemophilus influenza tipe B (Hib), tidak memberikan perlindungan terhadap virus corona baru. Virus Covid-19 adalah virus baru dan berbeda sehingga membutuhkan vaksin sendiri. Namun, vaksinasi terhadap penyakit pernapasan sangat dianjurkan untuk melindungi kesehatan Anda.</p>
    <p class="card-text">8. Mitos: secara rutin membilas hidung dengan saline membantu mencegah infeksi Covid-19
Tidak ada bukti bahwa mencuci hidung dengan saline secara teratur akan melindungi orang dari infeksi virus corona. Saline merupakan cairan larutan garam dan air yang digunakan pada perawatan kesehatan dilansir dari Medicine Net. Hanya ada beberapa bukti bahwa mencuci hidung dengan saline secara teratur dapat membantu orang pulih lebih cepat dari flu biasa. Namun, membilas hidung secara teratur belum terbukti mencegah infeksi pernapasan.</p>
    <p class="card-text">9. Mitos: makan bawang putih membantu mencegah infeksi Covid-19
Bawang putih adalah makanan sehat yang mungkin memiliki beberapa sifat antimikroba. Namun, tidak ada bukti dari wabah saat ini bahwa makan bawang putih telah melindungi orang dari virus Corona terbaru tersebut.</p>
    <p class="card-text">10. Mitos: memakai minyak wijen dapat menghalangi Covid-19 memasuki tubuh
Faktanya, minyak wijen tidak membunuh virus corona tersebut. Ada beberapa disinfektan kimia yang dapat membunuh Covid-19 di permukaan kulit tubuh, termasuk desinfektan berbasis pemutih atau klorin, baik pelarut, etanol 75 persen, asam perasetat dan kloroform. Namun, cairan-cairan tersebut memiliki sedikit dampak pada virus jika Anda menaruhnya di kulit atau di bawah hidung Anda ketika virus telah memasuki tubuh. Selain itu, bisa berbahaya jika menaruh bahan kimia ini di kulit Anda.</p>
    <p class="card-text">11. Mitos: Covid-19 memengaruhi orang yang lebih tua
Faktanya, orang-orang dari segala usia dapat terinfeksi oleh Covid-19. Orang yang lebih tua, dan orang-orang dengan kondisi medis yang sudah ada sebelumnya seperti asma, diabetes, dan penyakit jantung, tampaknya lebih rentan untuk menjadi sakit parah dengan virus. WHO menyarankan orang-orang dari segala usia untuk mengambil langkah-langkah melindungi diri dari virus, misalnya dengan mengikuti kebersihan tangan yang baik dan kebersihan pernapasan yang baik.</p>
    <p class="card-text">12. Mitos: antibiotik efektif dalam mencegah dan mengobati Covid-19
Antibiotik tidak bekerja melawan virus, melainkan hanya bakteri. Covid-19 adalah virus dan oleh karena itu, antibiotik tidak boleh digunakan sebagai sarana pencegahan atau pengobatan. Namun, jika Anda dirawat di rumah sakit sebab virus Corona tersebut, Anda mungkin menerima antibiotik karena koinfeksi bakteri mungkin terjadi. </p>
    <p class="card-text">13. Mitos: tidak ada obat khusus untuk mencegah dan mengobati paparan virus Covid-19
Sampai saat ini, belum ada obat khusus yang disarankan untuk mencegah atau mengobati virus corona baru atau Covid-19. Namun, mereka yang terinfeksi virus harus menerima perawatan yang tepat untuk meredakan dan mengobati gejala. Selain itu, mereka yang sakit parah harus mendapatkan perawatan suportif yang dioptimalkan.
</p>
    <p class="card-text">(13 Mitos dan Fakta Soal Virus Corona COVID-19 Menurut WHO dikutip dari tirto.id)</p>
