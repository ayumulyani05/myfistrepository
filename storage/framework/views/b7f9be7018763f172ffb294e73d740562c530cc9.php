    <div class="card-header" align="center" ><b>Gejala Covid-19</b></div>
    <div class="card-header" align="center" ><b>Kenali Gejala Covid Sebelum Anda Memeriksa Ke Dokter</b></div>
    <div class="card-body">
    <b><p class="card-text">Demam</p></b>
    <p>Demam merupakan salah satu gejala COVID-19 yang khas. Demam yang dialami berkisar pada suhu 38 derajat celcius atau lebih. Maka dari itu jika suhu tubuh sudah pada angka 37.5 derajat Celcius, jangan disepelekan.
Apalagi jika demam yang dialami terjadi setelah bepergian. Segera periksakan ke dokter dan lakukan isolasi mandiri. Memang belum tentu gejala COVID-19, tetapi demam sudah pasti terjadi karena infeksi.</p></b>
    <b><p>Batuk Kering</p></b>
    <p>Mengalami batuk kering yang tak kunjung berkesudahan harus diwaspadai. Paling tidak sudah berupaya melakukan pengobatan. Jika tak kunjung sembuh dan pulih, batuk kering yang dialami termasuk gejala COVID-19 yang khas.
Apalagi jika batuk kering yang dialami disertai dengan demam tinggi. Bisa jadi, infeksi virus Corona COVID-19 sudah terjadi mengingat persebarannya semakin masif. Batuk kering bisa berarti respon terhadap patogen asing yang masu dalam tubuh.</p>
    <b><p>Kehilangan Bau dan Rasa</p></b>
    <p>Ketika kehilangan bau dan rasa tiba-tiba, jangan dianggap masalah biasa. Apalagi kondisinya sedang pandemi COVID-19. Keduanya termasuk gejala COVID-19 yang khas pada kasus ringan dan sedang.
Anosmia merupakan gejala alami bagi beberapa pasien. Hingga ada yang sampai kehilangan nafsu makan karenanya. Paling tidak ada sekitar 30 persen pasien yang mengalami, seperti yang terjadi di Korea Selatan.</p>
    <b><p>Ruam kulit</p></b>
    <p>Kini gejala COVID-19 yang khas semakin terus bertambah. Menurut penelitian, ada 20 persen pasien yang terdiagnosis Corona COVID-19 dengan ruam merah pada kulitnya. Ruam merah yang dialami hampir mirip dengan cacar air. Bergelombang, gatal, dan iritasi seperti cacar.
Kulit yang mengalami ruam disertai gejala demam dan batuk kering, harus diwaspadai. Menurut para ahli, ruam merupakan gejala khas keempat setelah demam, batuk kering, dan kehilangan bau. Segera lakukan pemeriksaan agar infeksi tidak menjadi semakin parah kemudian.</p>
    <b><p>Bingung dan Mengigil</p></b>
    <p>Gejala COVID-19 yang khas selanjutnya membuat pasien bingung dan menggigil. Kebingungan terjadi ketika pasien yang terinfeksi Corona COVID-19 sulit menopang dirinya. Bibir pasien akan kebiru-biruan dan mengalami nyeri dada.
Lalu rasa menggigil yang dialami pasien lebih sering terjadi pada malam hari. Kondisi seperti ini harus diwaspadai betul, apalagi ketika pasien juga sempat melakukan perjalanan, demam, batuk kering, dan sesak napas.</p>
    <b><p>Pegal-Pegal</p></b>
    <p>Pegal-pegal lumrahnya hanya disebabkan karena masuk angin saja. Jangan salah, pegal kini menajdi gejala COVID-19 yang khas. Pegal merupakan sinyal ketika tubuh tengah memerangi infeksi virus.
Pegal biasanya dibarengi dengan demam dan meriang. Pada saat itu pula sistem kekebalan tubuh sedang berperang dan menyebabkan tubuh meradang. Memang mirip masuk angin biasa, tetapi karena sedang pandemi COVID-19 gejala seperti ini tidak boleh disepelekan.
Lakukan isolasi mandiri dan redakan dengan mengonsumsi obat pereda demam. Jika perlu segera lakukan pemeriksaan untuk penanganan lebih dini. Biasanya dokter akan memeberikan resep obat penurun panas untuk pemulihan jika infeksi yang dialami bukan COVID-19.</p>
    <b><p>Sesak Napas</p></b>
    <p>Gejala COVID-19 yang khas adalah sesak napas. Kondisi ini terjaid karena virus Corona COVID-19 menyerang saluran pernapasan dan bereplika di sana. Gejalanya tidak langsung muncul setelah pasien mengalami infeksi, tetapi bertahap.
Biasanya sesak napas terjadi pada minggu kedua setelah pasien terinfeksi virus Corona COVID-19. Tingkat keparahan sesak napas juga bergantung dari penyakit bawaan yang dimiliki penderitanya. Bahkan bagi yang tidak memiliki penyakit bawaan, masalah bronkitis dan pneumonia bisa terjadi karenanya.
Awal mula infeksi virus Corona COVID-19 adalah mirip infeksi virus influenza. Menyebabkan pilek, hidung tersumbat, sakit kepala, sakit tenggorokan, sampai demam. Bedanya, kini masalah flu tidak boleh disepelekan karena persebaran virus Corona COVID-19 semakin masif.</p>
    <b><p>Mata Merah</p></b>
    <p>Beberapa penelitian di negara dengan kasus infeksi tinggi seperti China, beberapa pasien mengalami mata merah. Kondisi ini bisa menjadi gejala COVID-19 yang khas bagi beberapa pasien saja, 1-3 persen.
Saat konjungtiva terjadi, penularan bisa lebih masif dilakukan oleh pasien yang mengalami. Tepatnya ketika masalah mata merah dibarengi dengan beberapa gejala lain seperti demam, batuk kering, dan sesak napas.
</p>
    <b><p>Kelelahan</p></b>
    <p>Selain badan yang pegal seperti orang meriang, gejala COVID-19 yang khas menyebabkan kelelahan parah. Kelelahan ini biasanya terjadi tanpa alasan yang jelas. Tepatnya ketika seseorang tak melakukan aktivitas apapun, tetapi merasakan tubuhnya sangat lemas.
Badan Kesehatan Dunia WHO melaporkan, 40 persen dari 6.000 pasien positif COVID-19 mengaku seperti mengalami kelelahan. Pada beberapa kasus, kelelahan ekstrem masih terjadi ketika virus hilang atau saat pemulihan dilakukan.</p>
  
